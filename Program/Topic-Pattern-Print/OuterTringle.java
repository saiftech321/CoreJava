package com.programs;

public class OuterTringle {

	public static void main(String[] args) {

		
		
		for(int i=1;i<=4;i++)
		{
			
			 
			for(int j=1;i<=7;i++)
			{
			
			if((i==4) || (i+j==5) || (i-j==3))
			
			       {
				
				      System.out.print("*");
			       }
			
			else 
			{
				System.out.print(" ");
				
			}
			System.out.println();
			}
			
			
			
			
			
			
		}
	}

}
